void VulkanRenderer::createEntityDescriptorPool(MaterialComponent& material) {
    VkDescriptorPoolSize poolSize{};
    poolSize.type = VK_DESCRIPTOR_TYPE_UNIFORM_BUFFER;
    poolSize.descriptorCount = 1;

    VkDescriptorPoolCreateInfo poolInfo{};
    poolInfo.sType = VK_STRUCTURE_TYPE_DESCRIPTOR_POOL_CREATE_INFO;
    poolInfo.poolSizeCount = 1;
    poolInfo.pPoolSizes = &poolSize;
    poolInfo.maxSets = 1;

    if (vkCreateDescriptorPool(device, &poolInfo, nullptr, &material.descriptorPool) != VK_SUCCESS) {
        throw std::runtime_error("failed to create descriptor pool for entity!");
    }
}

void VulkanRenderer::cleanup() 
{
    if(device != VK_NULL_HANDLE)
    {
        vkDeviceWaitIdle(device);
        cleanupSwapChain();
        
        if (descriptorPool != VK_NULL_HANDLE) {
            vkDestroyDescriptorPool(device, descriptorPool, nullptr);
            descriptorPool = VK_NULL_HANDLE;
        }

        if (descriptorSetLayout != VK_NULL_HANDLE) {
            vkDestroyDescriptorSetLayout(device, descriptorSetLayout, nullptr);
            descriptorSetLayout = VK_NULL_HANDLE;
        }

        if (uniformBuffer != VK_NULL_HANDLE) {
            vkDestroyBuffer(device, uniformBuffer, nullptr);
            uniformBuffer = VK_NULL_HANDLE;
        }

        if (uniformBufferMemory != VK_NULL_HANDLE) {
            vkFreeMemory(device, uniformBufferMemory, nullptr);
            uniformBufferMemory = VK_NULL_HANDLE;
        }

        for (size_t i = 0; i < MAX_FRAMES_IN_FLIGHT; i++) {
            if (renderFinishedSemaphores[i] != VK_NULL_HANDLE) {
                vkDestroySemaphore(device, renderFinishedSemaphores[i], nullptr);
                renderFinishedSemaphores[i] = VK_NULL_HANDLE;
            }
            if (imageAvailableSemaphores[i] != VK_NULL_HANDLE) {
                vkDestroySemaphore(device, imageAvailableSemaphores[i], nullptr);
                imageAvailableSemaphores[i] = VK_NULL_HANDLE;
            }
            if (inFlightFences[i] != VK_NULL_HANDLE) {
                vkDestroyFence(device, inFlightFences[i], nullptr);
                inFlightFences[i] = VK_NULL_HANDLE;
            }
        }

        if (commandPool != VK_NULL_HANDLE) {
            vkDestroyCommandPool(device, commandPool, nullptr);
            commandPool = VK_NULL_HANDLE;
        }

        if (device != VK_NULL_HANDLE) {
            vkDestroyDevice(device, nullptr);
            device = VK_NULL_HANDLE;
        }

        if (enableValidationLayers && debugMessenger != VK_NULL_HANDLE) {
            auto func = (PFN_vkDestroyDebugUtilsMessengerEXT)vkGetInstanceProcAddr(instance, "vkDestroyDebugUtilsMessengerEXT");
            if (func != nullptr) {
                func(instance, debugMessenger, nullptr);
                debugMessenger = VK_NULL_HANDLE;
            }
        }

        if (surface != VK_NULL_HANDLE) {
            vkDestroySurfaceKHR(instance, surface, nullptr);
            surface = VK_NULL_HANDLE;
        }

        if (instance != VK_NULL_HANDLE) {
            vkDestroyInstance(instance, nullptr);
            instance = VK_NULL_HANDLE;
        }        
    }
    
    scene.reset();
}

void VulkanRenderer::checkWireframeModeChange() {
    bool requested = requestedWireframe.load(std::memory_order_acquire);
    if (requested != m_wireframeMode) {
        std::lock_guard<std::mutex> lock(pipelineMutex);
        
        try {
            // Ensure device is idle before recreation
            vkDeviceWaitIdle(device);
            
            m_wireframeMode = requested;
            recreateGraphicsPipeline();
        }
        catch (const std::exception& e) {
            std::cerr << "Wireframe mode change failed: " << e.what() << std::endl;
            // Revert to previous state
            requestedWireframe.store(m_wireframeMode, std::memory_order_release);
        }
    }
}

