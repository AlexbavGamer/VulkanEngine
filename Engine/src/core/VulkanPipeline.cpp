void VulkanPipeline::bindScenePipeline(VkCommandBuffer commandBuffer)
{
    vkCmdBindPipeline(commandBuffer, VK_PIPELINE_BIND_POINT_GRAPHICS, sceneGraphicsPipeline);
}

